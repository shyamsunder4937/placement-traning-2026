package day3;

class Main {

    public static int demo(int a, int b) {
        return a + b;
    }

    public static int demo2(int a, int b) {
        return a - b;
    }

    public static int demo3(int a, int b) {
        return a * b;
    }

    public static int demo4(int a, int b) {
        return a / b;
    }

    public static  void main(String[] args) {


        System.out.println(demo(4, 5));
        System.out.println(demo2(4, 5));
        System.out.println(demo3(4, 5));
        System.out.println(demo4(4, 5));

        
    }
}